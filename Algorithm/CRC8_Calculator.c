/*********************************************************************************
 * @name    CRC8_Calc_Byte
 * @param   {unsigned char} val
 * @return  {-}
 * @brief   计算传入数值CRC值 返回1Byte结果(多用于查表)
*********************************************************************************/
static unsigned char CRC8_Calc_Byte(unsigned char val)
{
	unsigned char BitCnt;
	for (BitCnt = 8; BitCnt > 0; BitCnt--)
	{
		#if (CRC8_ENDIAN_TYPE == CRC8_BIG_ENDIAN)	// MSB first
		/* 若最高位为1则与多项式异或 左移1位 */
		if (val & 0x80)
			val = (val << 1) ^ POLY_NORMIAL_MSB;
		else
			val = (val << 1);
		
		#else	// LSB first
		/* 若最低位为1则与多项式异或 右移1位 */
		if (val & 0x01)
			val = (val >> 1) ^ POLY_NORMIAL_LSB;
		else
			val = (val >> 1);
		#endif
	}
	return val;
}

