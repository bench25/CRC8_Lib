/*********************************************************************************
 * @name    CRC8_Calc_Normal
 * @param   {unsigned char} *ptr
 * @param   {unsigned char} len
 * @return  {-}
 * @brief   纯计算法获取CRC8校验码 大端/小端格式
*********************************************************************************/
unsigned char CRC8_Calc_Normal(unsigned char Polynorimial_LSB, const unsigned char *ptr, unsigned char len)
{
	unsigned char BitCnt;
	unsigned char crc = 0;	/* 计算初值 */
	#if (CRC8_ENDIAN_TYPE == CRC8_BIG_ENDIAN)	// MSB first
		unsigned char Polynorimial_MSB;
		for (BitCnt = 0, Polynorimial_MSB = 0; BitCnt < 8; BitCnt++) {
			Polynorimial_MSB <<= 1;
			Polynorimial_MSB |= ((Polynorimial_LSB & (1 << BitCnt))? 1 : 0);
		}
	#endif

	while (len--)
	{
		crc ^= *ptr ++;		/* 每次先与需要计算的数据异或 计算完成指向下一数据 */
		for (BitCnt = 8; BitCnt > 0; BitCnt--)
		{
			#if (CRC8_ENDIAN_TYPE == CRC8_BIG_ENDIAN)	// MSB first
			/* 若最高位为1则与多项式异或 左移1位 */
			if (crc & 0x80)
				crc = (crc << 1) ^ Polynorimial_MSB;
			else
				crc = (crc << 1);
			
			#else	// LSB first
			/* 若最低位为1则与多项式异或 右移1位 */
			if (crc & 0x01)
				crc = (crc >> 1) ^ Polynorimial_LSB;
			else
				crc = (crc >> 1);
			#endif
		}
	}
	return (crc);
}

/*********************************************************************************
 * @name    CRC8_Calc_Table
 * @param   {unsigned char} *ptr
 * @param   {unsigned char} len
 * @return  {-}
 * @brief   查表法计算CRC8, 降低计算消耗时间 空间消耗增加
*********************************************************************************/
unsigned char CRC8_Calc_Table(const unsigned char *ptr, unsigned char len)
{
	unsigned char crc = 0;	/* 初值 */
	while (len--)
	{
		crc = cCRC8_Table[crc ^ *ptr ++];
	}
	return (crc);
}

